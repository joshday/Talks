macro ggplot2(ex)
	nm = tempname() * ".png"
	s = string(ex)
	s2 = "ggsave(\"$nm\", height=4)"
	esc(quote
		let 
			@R_str $s
			@R_str $s2
			LocalResource($nm) 
		end
	end)
end

