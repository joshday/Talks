# ‚ïî‚ïê‚ï° e3fdb994-7e6e-4e50-bf88-589fdcd2f565
md"""

- Let's implement a quantile function that uses Newton's method for finding roots:
	
$$\text{Solve for } x \text{:}\quad F_X(x) - q= 0$$

- where $F_X$ is CDF of random variable $X$.

$$x_{n+1} = x_n - \frac{F_X(x_n) - q}{F_X'(x_n)}$$


!!! note "What functions do we need?"
	- Something to initialize estimate with.
	- $F_X(x)$
	- $F_X'(x)$
"""

# ‚ïî‚ïê‚ï° c80072af-061b-435f-94ad-65f49dd9d3ee
function myquantile(dist, q)
	Œ∏ = mean(dist)
	for _ in 1:20 
		Œ∏ -= (cdf(dist, Œ∏) - q) / pdf(dist, Œ∏)
	end
	return Œ∏
end

# ‚ïî‚ïê‚ï° 45dc031f-7aff-40eb-9ea7-e48e82a5bcbd
md"""
#### Does it Work?

$(@bind dist Select([nothing, Normal(), Gamma(5,1), Uniform(0,1), Beta(7,2)]))
"""

# ‚ïî‚ïê‚ï° eed57094-890e-4274-a30d-64c125d54db7
let
	rng = 0.01:.01:.99
	if isnothing(dist) 
		nothing 
	else
		@show myquantile(dist, 0.5)
		@show quantile(dist, 0.5)
		p = Plot()(
			y=rng, x=quantile.(dist, rng), name="quantile"
		)(
			y=rng, x=myquantile.(dist, rng), line=(;dash="dot"),
			name="myquantile"
		)
		p.layout.title.text = string(dist)
		p.layout.xaxis.title.text = "x"
		p.layout.yaxis.title.text = "P(X) < x"
		p
	end
end

# ‚ïî‚ïê‚ï° a13c80bd-d3b5-4a9e-9173-7ad9b71151ff
md"""
!!! success "Key Takeaways"
	- **We didn't tell Julia anything about `dist`.**
      - We still get specialized code based on input types (JIT).
      - Input requires `mean`/`cdf`/`pdf` methods and our function will "just work".
	- Types don't own methods.  Methods can be added at any time.
	  - E.g. `SparseArrays` (and other packages) add their own `*` methods for types that represent certain matrix structures - gives you free speedups if you can exploit the structure (i.e. avoid every multiplication by 0).
"""

# ‚ïî‚ïê‚ï° 51b3e41c-be9f-4eee-b2b5-61c616b6956a
md"""
### Example: Free Speedup via `Diagonal`

- Suppose you write a function that works for any `AbstractMatrix` and later on discover the matrix is diagonal.
"""

# ‚ïî‚ïê‚ï° 6ff92ee0-08e4-4a65-a27c-7bb29264546c
let 
	# `Diagonal` doesn't store the off-diagonal elements
	x = Diagonal(randn(1000))

	# `Matrix` stores the off-diagonals (all are 0.0)
	x2 = Matrix(x)
	
	y = randn(1000)

	# Same function, different *methods*
	t1 = @elapsed x * y
	t2 = @elapsed x2 * y
	@info "Dense matrix multiply is $(round(t2/ t1, digits=2)) times slower than Diagonal."
end

